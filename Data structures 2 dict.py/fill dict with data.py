# pradedam nuo tuščio
auto = {}
print(auto)

# pildom duomenimis
auto["make"] = "Audi"
print(auto)  # {'make': 'Audi'}

auto["make"] = "BMW"
print(auto)  # {'make': 'BMW'}

auto["model"] = "x5"
print(auto)  # {'make': 'BMW', 'model': 'x5'}

auto["colors"] = ["red", "white", "black"]
print(auto)  # {'make': 'BMW', 'model': 'x5', 'colors': ['red', 'white', 'black']}