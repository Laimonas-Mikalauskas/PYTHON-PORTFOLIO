numbers = [10, 7, 50, 111]
empty = []

# len - list length , number of elements
print(len(numbers))  # 4
print(len(empty))  # 0

# works and with str type
print(len("ABC"))  # 3

# sum - sum of elements
print(sum(numbers))  # 178

# max - biggest number
print(max(numbers))  # 111

# min - smallest number
print(min(numbers))  # 7
